package com.company.homework.hw05;

public class ChangeTextUpperOrLower1 {
    public static void main(String[] args) {

        String proverb = "Beauty is in the eye of the beholder";

        String proverbUpper = proverb.toUpperCase();
        System.out.println(proverbUpper);

        String proverbLower = proverbUpper.toLowerCase();
        System.out.println(proverbLower);
        System.out.println(proverb.length());

    }
}

